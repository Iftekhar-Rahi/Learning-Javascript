// prefix Increment
let a = 1
let b = ++a
console.log(`a: ${a}`)
console.log(`b: ${b}`)
console.log('-----')
//Here, ++a is the pre-increment operator. This means that a will first be incremented, and then used in the expression.


// postfix Increment
let c = 1
let d = c++
console.log(`c: ${c}`)
console.log(`d: ${d}`)

// Here, x++ is the post-increment operator. This means that x will first be used in the expression, and then incremented